function getAgeRange(age) {
  switch (true) {
    case age === 0:
      return "0 years";
    case age === 1:
      return "1 years";
    case age >= 2 && age <= 3:
      return "2-3 years";
    case age >= 4 && age <= 8:
      return "4-8 years";
    case age >= 9 && age <= 13:
      return "9-13 years";
    case age >= 14 && age <= 18:
      return "14-18 years";
    case age >= 19 && age <= 70:
      return "19–70 years";
    default:
      return "> 70 years";
  }
}

export default function calculateVitaminIntake(vitamin, sex, age) {
  const ageRange = getAgeRange(age);
  const vitaminData = vitaminIntakeData[vitamin];
  return `${vitaminData[sex][ageRange]} ${vitaminData.unit}`;
}
